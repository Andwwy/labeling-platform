#!/usr/bin/env node
// Vercel build step: read MOTHERDUCK_TOKEN + MOTHERDUCK_DATABASE from the
// project's environment variables (configured in the Vercel dashboard) and
// emit config.js next to the HTML. The HTML loads config.js with a regular
// <script> tag before the module script, populating window.__LABELING_CONFIG.
//
// config.js is gitignored — it only exists in the Vercel build artifact, and
// optionally in local dev when you generate it manually.
const fs = require('fs');

const token = process.env.MOTHERDUCK_TOKEN;
const database = process.env.MOTHERDUCK_DATABASE || 'rules_in_the_wild';

if (!token) {
  console.error(
    'MOTHERDUCK_TOKEN not set. Configure it under Vercel project settings → ' +
    'Environment Variables (or export it locally before running this script).'
  );
  process.exit(1);
}

const payload = { MOTHERDUCK_TOKEN: token, MOTHERDUCK_DATABASE: database };
fs.writeFileSync(
  'config.js',
  '// AUTO-GENERATED by scripts/build-vercel-config.js. Do not commit.\n' +
  'window.__LABELING_CONFIG = ' + JSON.stringify(payload) + ';\n'
);
console.log(`Wrote config.js (DB: ${database})`);
